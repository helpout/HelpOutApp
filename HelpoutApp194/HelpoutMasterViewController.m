- (BOOL)peoplePickerNavigationController:
(ABPeoplePickerNavigationController *)peoplePicker
      shouldContinueAfterSelectingPerson:(ABRecordRef)person {
    
    [self displayPerson:person];
    [self dismissModalViewControllerAnimated:YES];
    
    //added
    NSString *firstname = (__bridge NSString *)ABRecordCopyValue(person, kABPersonFirstNameProperty);
    NSString *lastname = (__bridge NSString *)ABRecordCopyValue(person, kABPersonLastNameProperty);
    NSString *name = [NSString stringWithFormat:@"%@ %@", firstname, lastname];
    
    NSMutableArray *phoneNumbers = [[NSMutableArray alloc] init];
    ABMultiValueRef multiPhones = ABRecordCopyValue(person,kABPersonPhoneProperty);
    for(CFIndex i=0;i<ABMultiValueGetCount(multiPhones);++i) {
        CFStringRef phoneNumberRef = ABMultiValueCopyValueAtIndex(multiPhones, i);
        NSString *phoneNumber = (__bridge NSString *) phoneNumberRef;
        
        [phoneNumbers addObject:phoneNumber];
    }
    
    
    NSString *number =[phoneNumbers objectAtIndex:0];

    ContactInfo *contact = [[ContactInfo alloc] initWithName:name number:number];
    
    NSLog(@"name is %@", name);
    NSLog(@"number is %@", number);
    
    [_objects insertObject:contact atIndex:0];
    [_numbers insertObject:number atIndex:0];
    NSLog(@"Objects is %@", _objects);
    NSLog(@"Numbers is %@", _numbers);
    NSIndexPath *indexPath = [NSIndexPath indexPathForRow:0 inSection:0];
    [self.tableView insertRowsAtIndexPaths:[NSArray arrayWithObject:indexPath] withRowAnimation:UITableViewRowAnimationAutomatic];
    //end added
    
    return NO;
}

